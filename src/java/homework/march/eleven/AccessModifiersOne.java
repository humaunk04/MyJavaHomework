package java.homework.march.eleven;

public class AccessModifiersOne {

    // default example can see only in same package
    String a = "Juned";
    String b = "mamun";
    // public example can see any package
    public int c = 2;
    public int d = 5;
    // private example the code is only accessible within the declared class
    private int e = 5;
    private int f = 10;
    // protected example The code is accessible in the same package and subclasses
    // how to do subclasses in different package use extends.
    protected int g = 10;
    protected int h = 15;





}
